package Pertemuan7;

public class LatihanDeretGanjil {
public static void main(String[] args) {
    // Nilai awal dimulai dari 1 (bilangan ganjil pertama)
    int angka = 1;
    System.out.println("=== BILANGAN GANJIL 1-15 ===");
        // Perulangan selama angka kurang dari atau sama dengan 15
    while (angka <= 15) {
        // Menampilkan angka ganjil
        System.out.print(angka + " ");
        // Menambah 2 agar tetap menjadi bilangan ganjil berikutnya
        angka += 2;
}
}
}
