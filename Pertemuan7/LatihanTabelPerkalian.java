package Pertemuan7;

public class LatihanTabelPerkalian {
    public static void main(String[] args) {
         // Angka yang akan dibuat tabel perkaliannya
        int angka = 7;
        // Perulangan dari 1 sampai 12
        for (int i = 1; i <= 12; i++) {
        // Menampilkan hasil perkalian angka dengan i
        System.out.println("7 x " + i + " = " + (angka * i));
        }
    }
}
